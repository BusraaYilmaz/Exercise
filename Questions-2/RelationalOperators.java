package soruCozumu_1;

public class RelationalOperators {

	public static void main(String[] args) {
		 
		System.out.println(5+2==7);
		System.out.println(5*2==10);
		System.out.println(5*3==10);
		
		boolean esitMi=2*3==7;
		System.out.println(esitMi);
		
		boolean sonuc1=14*10==49;
		System.out.println(sonuc1);
		
		System.out.println(9+2>=11 || 9+2==1);
		System.out.println(9+2==10 || 8*2==15);
		System.out.println(9+2==11 && 9*2==18);
		System.out.println(9+2==11 && 9+2==9);

	}

}
